package introducao;

public class ImprimindoVariaveis {
    public static void main(String[] args) {
        int idade = 10; //Variável do tipo inteiro, o igual recebe o valor
        double salarioDouble = 4000;
        float salarioFloat = 4000F;
        byte idadeByte = 14;
        short idadeShort = 30345;
        boolean verdadeiro = true;
        boolean falso = false;
        long numeroGrande = 1000L;
        char caractere = 95; //2 bytes. Os números são traduzidos pela tabela unicode, ou colocar valor representando
        //hexadecimal (ex:/u0041).
        String nome = "Isaque";

        System.out.println("Dados impressos");
    }
}
