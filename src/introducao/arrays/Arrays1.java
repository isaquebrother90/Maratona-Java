package introducao.arrays;

public class Arrays1 {
    public static void main(String[] args) {
        int [] idade = new int[5];
        idade[0] = 56;
        idade[1] = 20;
        idade[2] = 5;
        idade[3] = 28;
        idade[4] = 90;
        System.out.println("A idade 1 é " + idade[0]);
        System.out.println("A idade 2 é " + idade[1]);
        System.out.println("A idade 3 é " + idade[2]);
        System.out.println("A idade 4 é " + idade[3]);
        System.out.println("A idade 5 é " + idade[4]);
    }
}
